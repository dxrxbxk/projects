#include <minishell.h>

void	free_tab(char ***tab)
{
	int	i;

	i = -1;
	if (*tab == NULL)
		return ;
	while ((*tab)[++i])
		free((*tab)[i]);
	free(*tab);
	*tab = NULL;
}
